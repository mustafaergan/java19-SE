package com.vektorel.overloading;

public class Fiat {
	private int id;
	private String name;
	
	public Fiat(){
		
	}
	
	public Fiat(int id,String name){
		
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
